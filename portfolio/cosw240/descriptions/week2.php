<h2>Homework 2: Site Organization and Content</h2>
<p>
This week we are going to continue organizing the content on our website and setting up the navigation structure. Do not worry about the actual site appearance yet. You'll have time next week to work on that aspect of the project.
</p>
<p>
Step 1: Create Categories and Tags for Content
</p>
<p>
Add at least two categories of posts for your website. Add at least five tags in your admin dashboard based on your plan for your blog content. Refer to Book 3/Chapter 5: Creating Categories and Tags if you need further explanation.
</p>
<p>
Organization of your content pieces is very important. Start broad and then use subcategories and tags to narrow things. For example, if your website is about video games you might want to separate it by device (such as PC Games, Console Games, etc) and then have sub categories within those broad categories. You can use tags that might be applied across all devices such as simulation, strategy, etc.
</p>
<p>
Step 2: Create two pages
</p>
<p>
Create and publish two new pages of content to your website. Each page should have a minimum of one image or video, at least 2 headings, and at least 3 paragraphs. Pages can be related to About this site, or talk about more specific aspect of your topic that might never need to be updated. For example, if you are doing a site about health and wellness, you might have a page dedicated to that in general, and then use your content posts to get more specific about the topics. Note: These are PAGES and not POSTS. Refer to Book 4/Chapter 2: Creating a Static Page if you need further explanation.
</p>
<p>
Step 3: Create Site Navigation
</p>
<p>
Add the two new pages of content to your navigation menu. Be sure your public website allows you to easily navigate between the home page, blog postings, and two new pages. 
</p>
<p>
Step 4: Create two blog posts
</p>
<p>
Create and publish two new blog posts of content related to the topic of your website. Your blog post sshould have a minimum of 3 headings, 4 paragraphs and 2 images. Assign a category to each blog post. Use a minimum of two tags for each post.
</p>