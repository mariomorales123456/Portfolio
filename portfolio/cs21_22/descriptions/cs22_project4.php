<p>
Give a recursive algorithm to compute the product of two positive integers, m and n, using only addition and subtraction.
</p>
<p>
Write your class with a method that returns an int with two int parameters.
</p>
<p>
Write a main method that tests your method multiplying 53 x 42.
</p>
<p>
As specified, use only addition and subtraction, not multiplication, division, or other math operations or methods. This project is intended to help you write recursive calls.
</p>
<p>
Hint: You need subtraction to count down from m or n and addition to do the arithmetic to get the right answer.
</p>
<p>
Suggestion: Use the debugger to trace recursive calls. Start with small numbers, say, 3 and 4, and watch the debugger. When the recursion involves hundreds of levels, it might not be worth tracing.
</p>
<p>
Please observe accepted Java standards for identifiers: names of classes are capitalized, identifiers start with lower case and continue with "camel case." Pick a style for Java braces and stick with it.
</p>
<p>
Use program comments.  Method and line comments with 'just right" amount of detail will be used for a small part of grading this project.
</p>
<p>
Put *your name* somewhere in the program comments.
</p>