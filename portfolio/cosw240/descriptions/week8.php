<h2>Discussion: WordPress Theme Frameworks and Builders</h2>

<p>
I'd like you to research a popular WordPress theme framework or theme builder that appeals to you. Go out and search, find some good articles with recommendations and choose a tool to research. These tools will have a website and many of the good ones will have a demo or tour for you to play with. Don't register for anything, don't download anything, just try to get to know a tool of your choice that allows you to customize a WordPress theme. 
</p>
