          <h2 class="text-center">Reflection on Bootstrap &amp; FlexBox</h2>
            <h3 class="text-center">Bootstrap</h3>
            <p>I'm still really new to Bootstrap. The previous class I took jQuery &amp; JavaScript and some of the script that was required took hours to get done. That same script takes minutes to set up with Bootstrap - that is incredible. It already has so much built in functionality that only minor adjustments to CSS need to be done. I can see how learning bootstrap is neccessary since a lot of future websites I see will have it built in.</p>
            <h3 class="text-center">FlexBox</h3>
            <p>FlexBox is wonderful for aligning. Before I learned flexbox the only way to center align was using margins. This could cause a lot of page issues, and most importantly it didn't center vertically which is problematic. Flexbox allows you to center center with 3 simple properties saving a lot of time and hassle. It can also be used to create columns although Bootstrap does that better. Before I learned Bootstrap I used flexbox primarily to do all my page alignment. </p>
