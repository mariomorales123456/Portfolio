<h2>Homework 7: Analytics</h2>


<p>
For this assignment we will be installing the following plugin:
</p>
<p>
ExactMetrics Google Analytics
Additionally, you will need to setup a Google Analytics account for your WordPress site.
</p>
<p>
To do that, first login to your Google email account if you have one via http://www.gmail.com (Links to an external site.) and then go to https://analytics.google.com/analytics/web/. (Links to an external site.) This will add the Google Analytics services to your existing Google account. Stay logged into your Google account while you work with the Google Analytics plugin for WordPress. If you are doing this at a public computer, be sure to LOG out of your Google account before you leave. 
</p>
<p>
 (Links to an external site.)If you do not have a gmail account, go directly to https://analytics.google.com/analytics/web/  (Links to an external site.)and create an account.
</p>
<p>
Step 1) Ensure you have created a Google Analytics account
</p>
<p>
Step 2) Go to your Plugins dashboard and search for ExactMetrics. Activate it.
</p>
<p>
Step 3) Go to the ExactMetrics Settings within the Plugins Dashboard and follow along with the instructions (Links to an external site.) to connect the plugin with your Google Analytics account.
</p>
