<h2>Homework 4: Plugins</h2>

<p>
Continue working with your development WordPress website by adding some plugins.
</p>
<p>
Find a contact form plugin to add to your site. Setup and install. Read the plugin documentation to see how to get the plugin setup to display on your website.
</p>
<p>
Search for a social sharing plugin OR search for an image gallery/lightbox plugin. Setup and install. Read the plugin documentation to see how to get the plugin setup to display on your website.
 </p>
<p>
Find a plugin of your choice (from the discussion forum activity) and install/setup your plugin. Read the documentation to see how to get the plugin setup on your website or admin pages. Not all plugins will be for the public side of your website. DO NOT INSTALL ANY SECURITY PLUGINS AT THIS POINT. They have been known to lock people out of their website.
</p>